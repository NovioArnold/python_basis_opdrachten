def celsius_to_fahrenheit(c):
    return 9/5 * c + 32

def fahrenheit_to_celsius(f):
    return 5/9 * (f - 32)

print(celsius_to_fahrenheit(-12))
print(fahrenheit_to_celsius(102))
print(celsius_to_fahrenheit(62.2))
print(fahrenheit_to_celsius(95))
